include console.inc

public IsDivide
extern D: dword, B: byte, S: byte, K: abs

.code

IsDivide proc
    mov EAX, D
    mov ECX, K
@U: SHR EAX, 1
    jC @bad
    loop @U
    cmp AH, 0
    jNE @bad
    mov B, AL
    jmp @ennd
@bad:
    mov B, 0
    outstrln offset S
@ennd:
    ret
IsDivide endp

    pause
    exit
end




